package ex01;

public class Funcionarios {
	private String nome;
	private float salario;
	
	public Funcionarios(String nome, float salario) {
		this.nome = nome;
		this.salario = salario;
	}
	public String getNome() {
		return this.nome;
	}
	public float getSalario() {
		return this.salario;
	}
	
	
}